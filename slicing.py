''' slicing:-
to obtain a part of values from the sequence (string,list,tuple)
syntax:- [startindex : endindex]
s[ : ] --> full string name is displayed
s[1:8]--> display 2 to 7 palces of the string
s[0:12:3]--> dispaly 1 to 12-3 values is displayed
'''

